#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	a;
	int	p;

	a = 1;
	p = 0;
	while (str[p] != '\0')
	{
		if (str[p] < 65 || (90 < str[p] && str[p] < 97) || str[p] > 122)
			a = 0;
		p++;
	}
	return (a);
}
