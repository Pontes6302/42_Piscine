#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	p;

	p = 0;
	while (str[p] != '\0')
	{
		if (str[p] > 96 && str[p] < 123)
			str[p] = str[p] - 32;
		p++;
	}
	return (str);
}
