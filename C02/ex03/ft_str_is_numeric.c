#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	a;
	int	p;

	a = 1;
	p = 0;
	while (str[p] != '\0')
	{
		if (str[p] < 48 || str[p] > 57)
			a = 0;
		p++;
	}
	return (a);
}
