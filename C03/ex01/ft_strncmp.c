#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (0);
	while ((s1[i] || s2[i]) && s1[i] == s2[i] && n != 1)
	{
		i++;
		n = n - 1;
	}
	return (s1[i] - s2[i]);
}
